/**
 * 扁平数组转树形结构数组
 * @param {array} list 扁平数组
 * @param {string} idKey id字段名
 * @param {string} parentKey 父节点id字段名
 * @param {string} childrenKey 子节点字段名
 * @returns {array} 树形结构数组
 */
const flatListToTree = (list, idKey = 'id', parentKey = 'parentId', childrenKey = 'children') => {
  const map = {} // 存储每个节点的索引
  const roots = [] // 存储根节点
  let node, i

  for (i = 0; i < list.length; i++) {
    map[list[i][idKey]] = i
    list[i][childrenKey] = []
  }

  for (i = 0; i < list.length; i++) {
    node = list[i]
    if (node[parentKey] && map[node[parentKey]]) {
      console.log(map[node[parentKey]])
      list[map[node[parentKey]]][childrenKey].push(node)
    } else {
      roots.push(node)
    }
  }

  return roots
}

/**
 * 树形结构数组转扁平数组
 * @param {*} tree 树形结构数组
 * @param {*} childrenKey 子节点字段名
 * @returns {array} 扁平数组
 */
const treeToFlatList = (tree, childrenKey = 'children') => {
  const lists = []
  let node, i
  for (let i = 0; i < tree.length; i++) {
    node = { ...tree[i] }
    delete node[childrenKey]
    console.log(node, tree[i])
    lists.push(node)
    if (tree[i][childrenKey] && tree[i][childrenKey].length > 0) {
      lists.push(...treeToFlatList(tree[i][childrenKey], childrenKey))
    }
  }

  return lists
}

